CREATE PARTITION FUNCTION Transaction_CreatedDate_PFN(datetime)  
AS  RANGE LEFT FOR VALUES ( 
'20111231 23:59:59.997',
'20121231 23:59:59.997',
'20131231 23:59:59.997',
'20141231 23:59:59.997',
'20151231 23:59:59.997'
)

CREATE PARTITION SCHEME Transaction_CreatedDate_PS
AS PARTITION Transaction_CreatedDate_PFN
TO (LIB_OldData, [PRIMARY], LIB_2013, LIB_2014, LIB_2015, LIB_2016);
